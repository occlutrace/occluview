//! "Repair mesh" kernel pipeline.
//!
//! Fixed pass order over a working copy of the input:
//! 1. weld exact-duplicate vertices ([`weld`]),
//! 2. drop degenerate triangles ([`degenerate`]),
//! 3. drop duplicate faces ([`degenerate`]),
//! 4. split non-manifold edges ([`manifold`]),
//! 5. split bowtie vertices ([`manifold`]),
//! 6. coherent orientation ([`orient`]),
//! 7. debris removal ([`debris`]),
//! 8. tiny-hole fill (shared hole machinery),
//! 9. unreferenced-vertex compaction,
//! 10. one final validate + normal rebuild.
//!
//! Invariants: no original vertex ever moves (weld snaps to a
//! representative's exact bits; other passes only delete, duplicate, or
//! reindex), rims longer than `tiny_hole_max_edges` stay open, and a run that
//! changed nothing returns a clone of the input untouched.

