/// Options for [`repair_mesh`].
#[derive(Copy, Clone, Debug, PartialEq)]
pub struct RepairOptions {
    /// Weld radius in mesh units (mm). The effective radius is additionally
    /// capped at `1e-3` of the mesh bounding-box diagonal.
    pub weld_epsilon: f32,
    /// When set, weld only vertices with bit-identical positions (apart from
    /// signed zero) and matching color/UV payloads. This preserves genuine
    /// micro-gaps for operations that may not close geometry speculatively.
    pub exact_weld_only: bool,
    /// Boundary rims with at most this many edges are filled; larger rims are
    /// the scan's natural boundary and stay open.
    pub tiny_hole_max_edges: usize,
    /// A component is debris only if its face count is below this fraction of
    /// the largest component's face count.
    pub debris_face_fraction: f32,
    /// ... and its bounding-box diameter is below this fraction of the whole
    /// mesh's bounding-box diagonal.
    pub debris_diameter_fraction: f32,
    /// Attribute policy for cap vertices generated by tiny-hole filling.
    pub attribute_policy: MeshEditAttributePolicy,
}

impl Default for RepairOptions {
    fn default() -> Self {
        Self {
            weld_epsilon: 1e-4,
            exact_weld_only: false,
            tiny_hole_max_edges: 32,
            debris_face_fraction: 0.02,
            debris_diameter_fraction: 0.10,
            attribute_policy: MeshEditAttributePolicy::default(),
        }
    }
}

impl RepairOptions {
    /// Validate option values before a repair run.
    ///
    /// # Errors
    /// Returns [`MeshEditError::InvalidOptions`] if a value is nonsensical.
    pub fn validate(self) -> Result<Self, MeshEditError> {
        if !self.weld_epsilon.is_finite() || self.weld_epsilon <= 0.0 {
            return Err(MeshEditError::InvalidOptions {
                reason: "weld_epsilon must be finite and greater than zero".to_string(),
            });
        }
        if self.tiny_hole_max_edges == 0 {
            return Err(MeshEditError::InvalidOptions {
                reason: "tiny_hole_max_edges must be greater than zero".to_string(),
            });
        }
        for (name, value) in [
            ("debris_face_fraction", self.debris_face_fraction),
            ("debris_diameter_fraction", self.debris_diameter_fraction),
        ] {
            if !value.is_finite() || !(0.0..=1.0).contains(&value) {
                return Err(MeshEditError::InvalidOptions {
                    reason: format!("{name} must be finite and within 0.0..=1.0"),
                });
            }
        }
        Ok(self)
    }
}

/// Summary of one [`repair_mesh`] run.
#[derive(Clone, Debug, Default, PartialEq, Eq)]
pub struct RepairReport {
    /// Input vertex count.
    pub input_vertices: usize,
    /// Input triangle count.
    pub input_triangles: usize,
    /// Output vertex count.
    pub output_vertices: usize,
    /// Output triangle count.
    pub output_triangles: usize,
    /// Vertices removed by welding onto a full-attribute-identical
    /// representative.
    pub welded_vertices: usize,
    /// Triangles removed for repeated indices or (relatively) zero area.
    pub removed_degenerate_triangles: usize,
    /// Duplicate faces removed (same- and opposite-winding together).
    pub removed_duplicate_triangles: usize,
    /// Edges with more than two incident faces that were split to border.
    pub split_nonmanifold_edges: usize,
    /// Vertices shared by multiple disjoint face fans that were split.
    pub split_bowtie_vertices: usize,
    /// Net triangle winding flips after per-component majority normalization.
    pub reoriented_triangles: usize,
    /// Closed components flipped wholesale to point outward.
    pub flipped_components: usize,
    /// Small distant components dropped as debris.
    pub removed_debris_components: usize,
    /// Triangles removed with debris components.
    pub removed_debris_triangles: usize,
    /// Tiny boundary rims closed with a cap.
    pub filled_holes: usize,
    /// Rims larger than `tiny_hole_max_edges`, left open. Informational —
    /// this is the scan's natural boundary, not damage.
    pub open_rims_left: usize,
    /// Unreferenced vertices compacted away (beyond welds).
    pub removed_unreferenced_vertices: usize,
    /// Genuinely non-simple or degenerate rims that could not be filled.
    pub warnings: Vec<MeshEditWarning>,
}

impl RepairReport {
    /// Whether any pass actually mutated mesh content. `open_rims_left` and
    /// `warnings` are informational and do not count.
    #[must_use]
    pub fn changed_content(&self) -> bool {
        self.welded_vertices > 0
            || self.removed_degenerate_triangles > 0
            || self.removed_duplicate_triangles > 0
            || self.split_nonmanifold_edges > 0
            || self.split_bowtie_vertices > 0
            || self.reoriented_triangles > 0
            || self.flipped_components > 0
            || self.removed_debris_components > 0
            || self.removed_debris_triangles > 0
            || self.filled_holes > 0
            || self.removed_unreferenced_vertices > 0
    }
}

/// A completed repair result.
#[derive(Clone, Debug, PartialEq)]
pub struct RepairResult {
    /// Repaired mesh buffers.
    pub mesh: MeshEditBuffers,
    /// Per-pass repair report.
    pub report: RepairReport,
}

/// Run the full repair pipeline on a triangle mesh.
///
/// See the module docs for the fixed pass order. If no pass changed content
/// ([`RepairReport::changed_content`] is `false`), the returned mesh is an
/// exact clone of the input — no compaction and no normal recompute happen.
///
/// # Errors
/// Returns typed validation errors for unsupported point clouds, malformed
/// triangle data, invalid options, or index overflow while splitting.
pub fn repair_mesh(
    mesh: &MeshEditBuffers,
    options: RepairOptions,
) -> Result<RepairResult, MeshEditError> {
    let options = options.validate()?;
    validate_face_edit_buffers(mesh.topology, &mesh.vertices, &mesh.indices)?;

    let mut report = RepairReport {
        input_vertices: mesh.vertices.len(),
        input_triangles: mesh.triangle_count(),
        ..RepairReport::default()
    };
    let mut work = mesh.clone();

    run_topology_passes(&mut work, &options, &mut report)?;
    run_fill_pass(&mut work, &options, &mut report)?;
    compact_unreferenced_vertices(&mut work, &mut report)?;

    if !report.changed_content() {
        report.output_vertices = report.input_vertices;
        report.output_triangles = report.input_triangles;
        return Ok(RepairResult {
            mesh: mesh.clone(),
            report,
        });
    }

    validate_triangle_mesh_data(&work.vertices, &work.indices)?;
    recompute_all_normals(&mut work.vertices, &work.indices)?;
    report.output_vertices = work.vertices.len();
    report.output_triangles = work.triangle_count();
    Ok(RepairResult { mesh: work, report })
}

/// Passes 1–7. Connected components are computed once and shared between the
/// orientation and debris passes (orientation only flips windings, which
/// leaves both triangle indexing and undirected edges untouched).
fn run_topology_passes(
    work: &mut MeshEditBuffers,
    options: &RepairOptions,
    report: &mut RepairReport,
) -> Result<(), MeshEditError> {
    weld::weld_duplicate_vertices(work, options.weld_epsilon, options.exact_weld_only, report)?;
    degenerate::remove_degenerate_triangles(work, report);
    degenerate::remove_duplicate_triangles(work, report);
    manifold::split_nonmanifold_edges(work, report)?;
    manifold::split_bowtie_vertices(work, report)?;

    let incidence = edge_incidence(&work.indices);
    let components = connected_components(&work.indices, &incidence);
    orient::orient_components(work, &components, &incidence, report);
    debris::remove_debris(work, &components, options, report);
    Ok(())
}

/// Pass 8: close tiny rims through the shared hole machinery. Oversize rims
/// are informational (`open_rims_left`); only genuinely non-simple or
/// degenerate rims become warnings.
fn run_fill_pass(
    work: &mut MeshEditBuffers,
    options: &RepairOptions,
    report: &mut RepairReport,
) -> Result<(), MeshEditError> {
    let fill_options = MeshEditOptions {
        compact_vertices: false,
        max_boundary_loop: options.tiny_hole_max_edges,
        attribute_policy: options.attribute_policy,
        // Repair gates tiny rims purely by edge count; never by mm perimeter
        // and never by the whole-mesh scan-border heuristic — a pinhole must
        // fill even when it happens to be the largest rim of a closed mesh.
        max_rim_perimeter_mm: None,
        protect_scan_border: false,
        // Repair keeps the legacy conservative walk (its debris/weld/degenerate
        // passes already ran); the Close Holes cut-line healing is opt-in and
        // OFF here so repair output stays byte-for-byte unchanged.
        heal_boundary_rims: false,
    };
    let (result, stats) = fill_holes_with_outcome(work, None, fill_options)?;
    *work = result.mesh;
    report.filled_holes += stats.filled;
    report.open_rims_left += stats.skipped_oversize + stats.skipped_border;
    for _ in 0..stats.skipped_degenerate {
        report.warnings.push(MeshEditWarning::DegenerateGeometry);
    }
    Ok(())
}

/// Pass 9: drop vertices no triangle references. Survivors keep their
/// ascending order, so a mesh with every vertex referenced passes through
/// bit-identical (the remap is the identity).
fn compact_unreferenced_vertices(
    work: &mut MeshEditBuffers,
    report: &mut RepairReport,
) -> Result<(), MeshEditError> {
    let mut referenced = vec![false; work.vertices.len()];
    for &index in &work.indices {
        referenced[index as usize] = true;
    }
    let survivors: Vec<usize> = referenced
        .iter()
        .enumerate()
        .filter_map(|(index, &kept)| kept.then_some(index))
        .collect();
    if survivors.len() == work.vertices.len() {
        return Ok(());
    }

    let removed = work.vertices.len() - survivors.len();
    let (vertices, remap) = super::copy_surviving_vertices(&work.vertices, &survivors)?;
    work.indices = super::remap_triangle_indices(&work.indices, &remap)?;
    work.vertices = vertices;
    report.removed_unreferenced_vertices += removed;
    Ok(())
}

/// A vertex position widened to `f64` for area/volume accumulation.
pub(super) fn dvec3_position(mesh: &MeshEditBuffers, index: u32) -> DVec3 {
    let position = mesh.vertices[index as usize].position;
    DVec3::new(
        f64::from(position[0]),
        f64::from(position[1]),
        f64::from(position[2]),
    )
}

/// Triangle area in `f64` (0 for non-finite input).
pub(super) fn triangle_area_f64(mesh: &MeshEditBuffers, triangle: usize) -> f64 {
    let base = triangle * 3;
    let a = dvec3_position(mesh, mesh.indices[base]);
    let b = dvec3_position(mesh, mesh.indices[base + 1]);
    let c = dvec3_position(mesh, mesh.indices[base + 2]);
    let doubled = (b - a).cross(c - a).length();
    if doubled.is_finite() {
        doubled * 0.5
    } else {
        0.0
    }
}
